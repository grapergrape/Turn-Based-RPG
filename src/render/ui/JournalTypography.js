export function ledgerText(ctx, value, x, y, color, tools, scale = 1) {
  const text = normalizeLedgerText(value);
  let dx = Math.round(x);
  const dy = Math.round(y);
  for (const character of text) {
    const glyph = LEDGER_FONT[character] ?? LEDGER_FONT[' '];
    for (let row = 0; row < glyph.length; row += 1) {
      for (let column = 0; column < glyph[row].length; column += 1) {
        if (glyph[row][column] === '1') {
          tools.rect(ctx, dx + column * scale, dy + row * scale, scale, scale, color);
        }
      }
    }
    dx += 5 * scale;
  }
}

export function ledgerTextWidth(value, scale = 1) {
  return normalizeLedgerText(value).length * 5 * scale;
}

export function clipLedgerText(value, maxCharacters) {
  const text = normalizeLedgerText(value);
  const max = Math.max(0, Math.floor(Number(maxCharacters) || 0));
  if (text.length <= max) return text;
  return max <= 3 ? text.slice(0, max) : `${text.slice(0, max - 3)}...`;
}

export function normalizeLedgerText(value) {
  return String(value ?? '')
    .replace(/[“”]/g, '"')
    .replace(/[‘’]/g, "'")
    .replace(/[–—]/g, '-')
    .replace(/×/g, 'X')
    .replace(/[^A-Za-z0-9:.,;!?+\-/'"()[\]<>%#* ]/g, ' ')
    .toUpperCase();
}
